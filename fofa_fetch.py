def third_stage():
    print("ğŸ§© ç¬¬ä¸‰é˜¶æ®µï¼šå¤šçº¿ç¨‹æ£€æµ‹ä»£è¡¨é¢‘é“ç”Ÿæˆ IPTV.txt å¹¶å†™å›å¯ç”¨ IP åˆ° ip/ç›®å½•ï¼ˆè¦†ç›–ï¼‰")

